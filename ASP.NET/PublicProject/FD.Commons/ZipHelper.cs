    /// <summary>   
    /// 适用与ZIP压缩   
    /// </summary>   
    public class ZipHelper
    {

        public static bool ZipDirectory(string strFile, string strZip, string password)
        {
            ZipOutputStream s = null;
            FileStream stream = null;
            try
            {
                if (strFile[strFile.Length - 1] != Path.DirectorySeparatorChar)
                    strFile += Path.DirectorySeparatorChar;
                if (File.Exists(strZip))
                    File.Delete(strZip);
                stream = File.Create(strZip);
                s = new ZipOutputStream(stream);
                if (!string.IsNullOrEmpty(password))
                    s.Password = password;
                s.SetLevel(6); // 0 - store only to 9 - means best compression
                ZipDirectory(strFile, s, strFile);
                s.Finish();
                s.Dispose();
                stream.Dispose();
                return true;
            }
            catch
            {
                s.Finish();
                s.Dispose();
                stream.Dispose();
                return false;
            }
        }

        private static void ZipDirectory(string strFile, ZipOutputStream s, string staticFile)
        {
            if (strFile[strFile.Length - 1] != Path.DirectorySeparatorChar) strFile += Path.DirectorySeparatorChar;
            //Crc32 crc = new Crc32();
            string[] filenames = Directory.GetFileSystemEntries(strFile);
            foreach (string file in filenames)
            {
                if (Directory.Exists(file))
                {
                    ZipDirectory(file, s, staticFile);
                }
                else // 否则直接压缩文件
                {
                    FileStream fs = File.OpenRead(file);
                    string tempfile = file.Substring(staticFile.LastIndexOf("\\") + 1);
                    ZipEntry entry = new ZipEntry(tempfile);

                    entry.DateTime = DateTime.Now;
                    entry.Size = fs.Length;
                    //crc.Reset();
                    //crc.Update(buffer);
                    //entry.Crc = crc.Value;
                    s.PutNextEntry(entry);
                    byte[] buffer = new byte[4 * 1024];
                    int sourceBytes;
                    do
                    {
                        sourceBytes = fs.Read(buffer, 0, buffer.Length);
                        s.Write(buffer, 0, sourceBytes);
                        s.Flush();
                    } while (sourceBytes > 0);
                    fs.Close();
                }
            }
        }



        #region 解压

        /// <summary>   
        /// 解压功能(解压压缩文件到指定目录)   
        /// </summary>   
        /// <param name="fileToUnZip">待解压的文件</param>   
        /// <param name="zipedFolder">指定解压目标目录</param>   
        /// <param name="password">密码</param>   
        /// <returns>解压结果</returns>   
        public static bool UnZipFile(byte[] bytes, string zipedFolder, string password)
        {
            bool result = true;
            FileStream fs = null;
            ZipInputStream zipStream = null;
            ZipEntry ent = null;
            string fileName;

            if (!Directory.Exists(zipedFolder))
                Directory.CreateDirectory(zipedFolder);

            try
            {
                Stream stream = new MemoryStream(bytes);
                zipStream = new ZipInputStream(stream);
                if (!string.IsNullOrEmpty(password)) zipStream.Password = password;
                while ((ent = zipStream.GetNextEntry()) != null)
                {
                    if (!string.IsNullOrEmpty(ent.Name))
                    {
                        fileName = Path.Combine(zipedFolder, ent.Name);
                        fileName = fileName.Replace('/', '\\');//change by Mr.HopeGi   

                        if (fileName.EndsWith("\\"))
                        {
                            Directory.CreateDirectory(fileName);
                            continue;
                        }

                        fs = File.Create(fileName);
                        int size = 2048;
                        byte[] data = new byte[size];
                        while (true)
                        {
                            size = zipStream.Read(data, 0, data.Length);
                            if (size > 0)
                                fs.Write(data, 0, data.Length);
                            else
                                break;
                        }
                    }
                }
            }
            catch
            {
                result = false;
            }
            finally
            {
                if (fs != null)
                {
                    fs.Close();
                    fs.Dispose();
                }
                if (zipStream != null)
                {
                    zipStream.Close();
                    zipStream.Dispose();
                }
                if (ent != null)
                {
                    ent = null;
                }
                GC.Collect();
                GC.Collect(1);
            }
            return result;
        }

